// Estrutura de repetição com teste lógico no inicio
let contador = 1
while (contador <= 3) {
    console.log(`Passo ${contador }`)
    contador++
}

// Estrutura de repetição com teste lógico no final
let contador2 = 1
do {
    console.log(`NovoPasso ${contador2}`)
    contador2++
}   while (contador2 <= 3)
